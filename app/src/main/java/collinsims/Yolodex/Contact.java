public class Contact {
    private String Name;
    private String Company;
    private String Address;
    private String Category;
    private int Rating;

    public Contact(String Name, String Company, String Address, String Category, int Rating){
        this.Name = Name;
        this.Company = Company;
        this.Address = Address;
        this.Category = Category;
        this.Rating = Rating;
    }
}
